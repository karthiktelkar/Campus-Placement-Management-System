    public boolean update(){
        try{
            String sid,com,pak,yr;
            
             sid=txtSid.getText(); 
             com=txtcom.getText();
            pak=txtpak.getText();
            yr=year.getText();
             
       Class.forName("com.mysql.jdbc.Driver");
           Connection con=DriverManager.getConnection("jdbc:mysql://localhost/campus","root","");
           Statement st=con.createStatement();
            String sql = "update placement set cname = '"+com+"',package='"+pak+"',year='"+yr+"' where student_usn = '"+sid+"' ";
            
            st.executeUpdate(sql);
            
            JOptionPane.showMessageDialog(this, "update successfull");
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "update failed");
        }
        return false;
    }
    
    public boolean add(){
        try
        {
           String q,sid,fn,ln,per,ph,em,com,pak,yr;
           sid=txtSid.getText();
            fn=txtFname.getText();
            ln=txtLname.getText();
            per=txtPer.getText();
            ph=txtPhone.getText();
            em=txtEmail.getText();
            com=txtcom.getText();
            pak=txtpak.getText();
            yr=year.getText();
           
           
          Connection con = DBConnection.getConnection();
            String sql = "insert into placement values(?,?,?,?,?,?,?,?,?)";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, sid);
            pst.setString(2,fn );
            pst.setString(3,ln);
            pst.setString(8,pak);
            pst.setString(4,per);
            pst.setString(5,ph);
            pst.setString(6,em);
            pst.setString(7,com);
            pst.setString(9,yr);
            
            pst.execute();
            
            JOptionPane.showMessageDialog(this, "added successfull");
        }
        catch(Exception e)
        {
               JOptionPane.showMessageDialog(this, "failed to add");
        }
        return true;
    }

